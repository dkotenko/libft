#include "libft.h"

char	*ft_strupr(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (ft_isalpha(s[i]))
			s[i] = ft_toupper(s[i]);
		i++;
	}
	return (s);
}
